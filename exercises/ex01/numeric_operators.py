"""Practicing using numeric operators."""

number_1: int = input("Left-hand side: ")
number_2: int = input("Right-hand side: ")

print(number_1 + " ** " + number_2 + " is " + str(int(number_1) ** int(number_2)))

print(number_1 + " / " + number_2 + " is " + str(int(number_1) / int(number_2)))

print(number_1 + " // " + number_2 + " is " + str(int(number_1) // int(number_2)))

print(number_1 + " % " + number_2 + " is " + str(int(number_1) % int(number_2)))

